def generate_profiles_yml(config: dict) -> str:
    """Generate profiles.yml based on SQL_AUTH_TYPE environment variable.

    Supports: windows, sql, entra_interactive, entra_sp.
    All values read from environment — no hardcoded credentials.
    """
    auth_type = os.environ.get('SQL_AUTH_TYPE', 'sql' if os.environ.get('SQL_USER') else 'windows')
    sql_server = os.environ.get('SQL_SERVER', 'localhost')
    sql_driver = os.environ.get('SQL_DRIVER', 'ODBC Driver 17 for SQL Server')
    encrypt = os.environ.get('SQL_ENCRYPT', 'false').lower() == 'true'
    trust_cert = os.environ.get('SQL_TRUST_CERT', 'true').lower() != 'false'

    if auth_type == 'windows':
        auth_block = f"""      trusted_connection: true
      trust_cert: {str(trust_cert).lower()}"""

    elif auth_type == 'sql':
        sql_user = os.environ.get('SQL_USER', '')
        sql_password = os.environ.get('SQL_PASSWORD', '')
        auth_block = f"""      user: "{sql_user}"
      password: "{sql_password}"
      trust_cert: {str(trust_cert).lower()}"""

    elif auth_type == 'entra_interactive':
        auth_block = """      authentication: ActiveDirectoryInteractive
      encrypt: true
      trust_cert: false"""

    elif auth_type == 'entra_sp':
        tenant_id = os.environ.get('AZURE_TENANT_ID', '')
        client_id = os.environ.get('AZURE_CLIENT_ID', '')
        client_secret = os.environ.get('AZURE_CLIENT_SECRET', '')
        auth_block = f"""      authentication: ServicePrincipal
      tenant_id: "{tenant_id}"
      client_id: "{client_id}"
      client_secret: "{client_secret}"
      encrypt: true
      trust_cert: false"""

    else:
        auth_block = """      trusted_connection: true
      trust_cert: true"""

    return f"""# dbt Profile Configuration for {config["display_name"]}
# Auto-generated — auth type: {auth_type}
# WARNING: Do NOT commit this file to version control (it's in .gitignore)

{config["profile_name"]}:
  target: dev
  outputs:
    dev:
      type: sqlserver
      driver: "{sql_driver}"
      server: {sql_server}
      port: 1433
      database: {config["database"]}
      schema: {config["dbt_schema"]}
      encrypt: {str(encrypt).lower()}
{auth_block}
"""


