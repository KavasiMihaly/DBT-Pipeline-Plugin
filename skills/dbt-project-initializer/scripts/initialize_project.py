def generate_claude_md(config: dict, template_path: Path = None) -> str:
    """Generate CLAUDE.md content from template file.

    Args:
        config: Dictionary with project configuration values
        template_path: Optional path to template file. If not provided,
                      looks for template in standard location.

    Returns:
        CLAUDE.md content with placeholders replaced
    """
    # Determine template path
    if template_path is None:
        # Template must be in skill's templates folder
        script_dir = Path(__file__).parent
        template_path = script_dir.parent / "templates" / "data-project-CLAUDE.md"

    # Require template file to exist - no fallback
    if not template_path.exists():
        print()
        print("ERROR: Template file not found!")
        print(f"  Expected: {template_path}")
        print()
        print("The template file is required to generate CLAUDE.md.")
        print("Ensure the skill folder contains: templates/data-project-CLAUDE.md")
        print()
        raise FileNotFoundError(f"Required template not found: {template_path}")

    # Read template and replace placeholders
    template_content = template_path.read_text(encoding='utf-8')

    replacements = {
        "{{PROJECT_DISPLAY_NAME}}": config["display_name"],
        "{{CREATED_DATE}}": datetime.now().strftime('%Y-%m-%d'),
        "{{PROJECT_DESCRIPTION}}": config["description"],
        "{{DEFAULT_SCHEMA}}": config["schema"],
        "{{DATABASE_NAME}}": config["database"],
    }

    result = template_content
    for placeholder, value in replacements.items():
        result = result.replace(placeholder, value)

    return result


def run_setup_script(target_path: Path, skip_venv: bool = False) -> bool:
    """Run the setup environment script."""
    setup_script = target_path / "0 - Architecture Setup" / "setup_environment.ps1"

    if not setup_script.exists():
        print(f"  Warning: Setup script not found at {setup_script}")
        return False

    if skip_venv:
        print("  Skipping virtual environment setup (--skip-venv flag)")
        return True

    print("\n  Running environment setup...")
    try:
        result = subprocess.run(
            ["powershell", "-ExecutionPolicy", "Bypass", "-File", str(setup_script)],
            cwd=str(target_path),
            capture_output=True,
            text=True,
            timeout=600  # 10 minute timeout
        )

        if result.returncode == 0:
            print("  Environment setup completed successfully")
            return True
        else:
            print(f"  Warning: Setup script returned non-zero exit code: {result.returncode}")
            if result.stderr:
                print(f"  Stderr: {result.stderr[:500]}")
            return False

    except subprocess.TimeoutExpired:
        print("  Warning: Setup script timed out")
        return False
    except Exception as e:
        print(f"  Warning: Could not run setup script: {e}")
        return False


def run_dbt_deps(target_path: Path) -> bool:
    """Run dbt deps to install packages after project creation.

    This ensures that dbt packages (like dbt_utils) are installed and ready
    for use by downstream agents, without requiring manual intervention.
    """
    data_pipeline_path = target_path / "3 - Data Pipeline"
    venv_dbt = target_path / ".venv" / "Scripts" / "dbt.exe"

    # Check if dbt is available in the virtual environment
    if not venv_dbt.exists():
        print("  Warning: dbt not found in virtual environment, skipping dbt deps")
        return False

    print("\n  Installing dbt packages (dbt deps)...")
    try:
        result = subprocess.run(
            [str(venv_dbt), "deps"],
            cwd=str(data_pipeline_path),
            capture_output=True,
            text=True,
            timeout=120  # 2 minute timeout
        )

        if result.returncode == 0:
            print("  dbt packages installed successfully")
            return True
        else:
            print(f"  Warning: dbt deps returned non-zero exit code: {result.returncode}")
            if result.stderr:
                print(f"  Stderr: {result.stderr[:500]}")
            return False

    except subprocess.TimeoutExpired:
        print("  Warning: dbt deps timed out")
        return False
    except Exception as e:
        print(f"  Warning: Could not run dbt deps: {e}")
        return False


